package number_system_dev;
class A2
{
	public static void main(String[] args) 
	{
		int num1 = 1234;
		int num2 = num1 / 10;
		int num3 = num1 / 100;
		int num4 = num1 / 1000;
		int num5 = num1 / 10000;
		System.out.println(num1);  // 1234
		System.out.println(num2);  // 123
		System.out.println(num3);  // 12
		System.out.println(num4);  // 1
		System.out.println(num5);  // 0
	}
}

